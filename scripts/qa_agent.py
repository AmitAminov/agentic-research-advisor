def _extract_json_object(text: str) -> dict[str, Any] | None:
    """Best-effort: pull the first balanced {...} JSON object out of text."""
    start = text.find("{")
    while start != -1:
        depth = 0
        for i in range(start, len(text)):
            c = text[i]
            if c == "{":
                depth += 1
            elif c == "}":
                depth -= 1
                if depth == 0:
                    chunk = text[start:i + 1]
                    try:
                        obj = json.loads(chunk)
                        if isinstance(obj, dict):
                            return obj
                    except Exception:  # noqa: BLE001
                        break
        start = text.find("{", start + 1)
    return None


def read_verdict(verdict_path: Path, log_path: Path) -> tuple[dict[str, Any] | None, str]:
    """Load the verdict JSON claude was asked to write; fall back to its log."""
    raw = read_json(verdict_path)
    if isinstance(raw, dict):
        return raw, "verdict-file"
    # fall back: try to recover a JSON object from the run log
    try:
        log_text = log_path.read_text(encoding="utf-8", errors="replace")
    except Exception:  # noqa: BLE001
        log_text = ""
    recovered = _extract_json_object(log_text)
    if recovered:
        return recovered, "recovered-from-log"
    return None, "missing"


def normalize_verdict(raw: dict[str, Any] | None) -> dict[str, Any]:
    """Coerce whatever came back into a stable verdict dict with a 'passed' bool."""
    v: dict[str, Any] = {"verdict": "fail", "summary": "", "checks": {}, "issues": []}
    if isinstance(raw, dict):
        vv = str(raw.get("verdict", "")).strip().lower()
        v["verdict"] = "pass" if vv == "pass" else "fail"
        v["summary"] = str(raw.get("summary", ""))
        if isinstance(raw.get("checks"), dict):
            v["checks"] = raw["checks"]
        issues = raw.get("issues")
        if isinstance(issues, list):
            norm_issues = []
            for it in issues:
                if isinstance(it, dict):
                    sev = str(it.get("severity", "major")).strip().lower()
                    if sev not in SEVERITIES:
                        sev = "major"
                    norm_issues.append({
                        "severity": sev,
                        "area": str(it.get("area", "")),
                        "file": str(it.get("file", "")),
                        "description": scrub(str(it.get("description", ""))),
                        "suggested_fix": scrub(str(it.get("suggested_fix", ""))),
                    })
                else:
                    norm_issues.append({"severity": "major", "area": "",
                                        "file": "", "description": scrub(str(it)),
                                        "suggested_fix": ""})
            v["issues"] = norm_issues
    # a verdict is a genuine PASS only if it says pass AND has no blocking issues
    blocking = [i for i in v["issues"] if i["severity"] in ("critical", "major")]
    v["blocking_count"] = len(blocking)
    v["passed"] = (v["verdict"] == "pass" and len(blocking) == 0)
    return v


def fail_verdict(reason: str) -> dict[str, Any]:
    """Synthesise a failing verdict when the QA pass itself did not deliver one."""
    return normalize_verdict({
        "verdict": "fail",
        "summary": reason,
        "checks": {},
        "issues": [{"severity": "critical", "area": "qa-harness",
                    "file": "", "description": reason,
                    "suggested_fix": "Re-run QA; ensure the claude CLI is reachable "
                                     "and the webapp was built."}],
    })


# --------------------------------------------------------------------------- #
# QA + developer passes
# --------------------------------------------------------------------------- #
def qa_pass(claude_exe: str, repo_root: Path, webapp: Path, new_ids: list[str],
            status: dict[str, Any] | None, qa_dir: Path, minutes: int,
            stamp: str, itno: int) -> dict[str, Any]:
    verdict_path = qa_dir / f"verdict_{stamp}_iter{itno}.json"
    logf = qa_dir / f"qa_{stamp}_iter{itno}.log"
    # ensure a stale verdict from a previous iteration is not misread
    if verdict_path.exists():
        try:
            verdict_path.unlink()
        except Exception:  # noqa: BLE001
            pass
    prompt = QA_PROMPT_TEMPLATE.format(
        webapp_dir=str(webapp),
        new_sections=format_new_sections(new_ids, status),
        status_summary=format_status_summary(status),
        verdict_path=str(verdict_path),
    )
    run_status, code = run_claude_with_retry(claude_exe, repo_root, repo_root,
                                             prompt, minutes, logf)
    if run_status == "claude-not-found":
        v = fail_verdict("claude CLI not found — cannot run QA.")
    elif run_status == "timeout":
        raw, src = read_verdict(verdict_path, logf)
        v = normalize_verdict(raw) if raw else fail_verdict(
            f"QA pass timed out after {minutes}m and left no verdict.")
    elif run_status == "api-error":
        v = fail_verdict(
            "QA claude run failed on a transient API/limit error after retries: "
            f"{_transient_failure(logf) or '(see log)'}")
    else:
        raw, src = read_verdict(verdict_path, logf)
        v = normalize_verdict(raw) if raw is not None else fail_verdict(
            "QA pass produced no parseable verdict. "
            f"Last claude output: {tail_file(logf, 3) or '(empty log)'}")
    v["_run"] = {"status": run_status, "code": code, "log": str(logf),
                 "verdict_file": str(verdict_path)}
    return v


def dev_pass(claude_exe: str, repo_root: Path, build_script: Path,
             verdict: dict[str, Any], qa_dir: Path, minutes: int,
             stamp: str, itno: int) -> dict[str, Any]:
    logf = qa_dir / f"dev_{stamp}_iter{itno}.log"
    issues = [i for i in verdict.get("issues", [])
              if i["severity"] in ("critical", "major")] or verdict.get("issues", [])
    issue_list = "\n".join(
        f"  - [{i['severity']}] ({i['area']}) {i['file']}: {i['description']}"
        + (f"\n      suggested fix: {i['suggested_fix']}" if i.get("suggested_fix") else "")
        for i in issues) or "  (no explicit issues listed; improve overall correctness)"
    prompt = DEV_PROMPT_TEMPLATE.format(
        build_script=str(build_script),
        qa_summary=verdict.get("summary", "") or "(no summary)",
        issue_list=issue_list,
    )
    run_status, code = run_claude_with_retry(claude_exe, repo_root, repo_root,
                                             prompt, minutes, logf)
    return {"status": run_status, "code": code, "log": str(logf),
            "issues_targeted": len(issues)}


