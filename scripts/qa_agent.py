def write_report(qa_dir: Path, stamp: str, report: dict[str, Any]) -> tuple[Path, Path]:
    json_path = qa_dir / f"qa_report_{stamp}.json"
    md_path = qa_dir / f"qa_report_{stamp}.md"
    write_json(json_path, report)

    lines: list[str] = []
    lines.append(f"# Web app QA report — {stamp}")
    lines.append("")
    final = "PASS ✅" if report["passed"] else "FAIL ❌"
    lines.append(f"**Final verdict:** {final}   "
                 f"(after {report['iterations_run']} / {report['max_iters']} iteration(s))")
    lines.append("")
    lines.append(f"- webapp: `{report['webapp']}`")
    lines.append(f"- new paper sections verified: "
                 f"{', '.join(report['new_ids']) if report['new_ids'] else '(none / whole-site)'}")
    lines.append(f"- fix loop enabled: {report['fix_enabled']}")
    lines.append("")
    for it in report["iterations"]:
        n = it["iteration"]
        v = it["verdict"]
        lines.append(f"## Iteration {n} — {'PASS' if v['passed'] else 'FAIL'}")
        if not it.get("build_ok", True):
            lines.append(f"- build: FAILED — {it.get('build_tail', '')}")
        else:
            lines.append("- build: ok")
        lines.append(f"- QA run: {v.get('_run', {}).get('status')} "
                     f"(log `{v.get('_run', {}).get('log')}`)")
        if v.get("summary"):
            lines.append(f"- summary: {v['summary']}")
        if v.get("checks"):
            chk = ", ".join(f"{k}={val}" for k, val in v["checks"].items())
            lines.append(f"- checks: {chk}")
        if v.get("issues"):
            lines.append(f"- issues ({v.get('blocking_count', 0)} blocking):")
            for i in v["issues"]:
                lines.append(f"    - **[{i['severity']}]** ({i['area']}) "
                             f"`{i['file']}` — {i['description']}")
        else:
            lines.append("- issues: none")
        if it.get("dev"):
            d = it["dev"]
            lines.append(f"- developer pass: {d['status']} "
                         f"(targeted {d['issues_targeted']} issue(s), log `{d['log']}`)")
        lines.append("")
    md_path.write_text(scrub("\n".join(lines)), encoding="utf-8")
    return json_path, md_path


# --------------------------------------------------------------------------- #
# main
# --------------------------------------------------------------------------- #
def main() -> int:
    here = Path(__file__).resolve().parent
    ap = argparse.ArgumentParser(
        description="Claude-driven QA loop for the reproduction web app (component 5).")
    ap.add_argument("--config", type=Path, default=here.parent / "config.json")
    ap.add_argument("--max-iters", type=int, default=3,
                    help="max QA->fix->rebuild cycles before giving up (default 3)")
    ap.add_argument("--qa-minutes", type=int, default=15,
                    help="wall-clock budget for each QA claude pass (default 15)")
    ap.add_argument("--dev-minutes", type=int, default=25,
                    help="wall-clock budget for each developer claude pass (default 25)")
    ap.add_argument("--new-ids", nargs="*", default=None,
                    help="explicit new paper ids (e.g. DL-slug) to verify; "
                         "default: diff against the last QA snapshot")
    ap.add_argument("--no-fix", action="store_true",
                    help="report only; never invoke the developer pass")
    ap.add_argument("--no-initial-build", action="store_true",
                    help="do not rebuild the site before the first QA pass")
    ap.add_argument("--run-tests", action="store_true",
                    help="rebuild with per-paper pytest badges (slower)")
    args = ap.parse_args()

    if not args.config.exists():
        print(f"[fatal] config not found: {args.config}")
        return 2
    cfg = load_config(args.config)  # READ-ONLY

    global _TOKEN_FILE
    _TOKEN_FILE = pipeline_paths.token_file(cfg)

    repo_root = pipeline_paths.repo_root(cfg)
    python_exe = pipeline_paths.python_exe(cfg)
    claude_exe = pipeline_paths.claude_exe(cfg)
    webapp = repo_root / "webapp"
    build_script = here / "build_webapp.py"
    qa_dir = repo_root / "state" / "qa"
    qa_dir.mkdir(parents=True, exist_ok=True)
    snapshot_path = qa_dir / "status_snapshot.json"

    stamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    print(f"[qa] webapp={webapp}")
    print(f"[qa] max_iters={args.max_iters} qa_minutes={args.qa_minutes} "
          f"dev_minutes={args.dev_minutes} fix={'off' if args.no_fix else 'on'}")

    # optional initial build so the site reflects the latest reproductions
    if not args.no_initial_build:
        ok, tail = run_build(python_exe, build_script, repo_root, args.run_tests,
                             qa_dir / f"build_{stamp}_iter0.log")
        print(f"[qa] initial build: {'ok' if ok else 'FAILED'}")
        if not ok:
            print(f"[qa] build tail: {tail}")

    snapshot = read_json(snapshot_path)
    status = load_status(webapp)
    new_ids = compute_new_ids(status, snapshot, args.new_ids)
    print(f"[qa] {len(new_ids)} new paper section(s) to verify: "
          f"{', '.join(new_ids) if new_ids else '(none / whole-site)'}")

    report: dict[str, Any] = {
        "stamp": stamp,
        "webapp": str(webapp),
        "repo_root": str(repo_root),
        "new_ids": new_ids,
        "max_iters": args.max_iters,
        "fix_enabled": not args.no_fix,
        "iterations": [],
        "iterations_run": 0,
        "passed": False,
    }

    passed = False
    for itno in range(1, args.max_iters + 1):
        print(f"\n[qa] === iteration {itno}/{args.max_iters} ===")
        status = load_status(webapp)  # refresh after any rebuild
        build_ok, build_tail = True, ""
        if itno > 1:
            # a fresh build already ran at the end of the previous iteration;
            # nothing to do here.
            pass

        verdict = qa_pass(claude_exe, repo_root, webapp, new_ids, status,
                          qa_dir, args.qa_minutes, stamp, itno)
        it_entry: dict[str, Any] = {
            "iteration": itno, "verdict": verdict,
            "build_ok": build_ok, "build_tail": build_tail,
        }
        print(f"[qa] verdict: {'PASS' if verdict['passed'] else 'FAIL'} "
              f"({verdict.get('blocking_count', 0)} blocking issue(s), "
              f"run={verdict.get('_run', {}).get('status')})")

        if verdict["passed"]:
            report["iterations"].append(it_entry)
            report["iterations_run"] = itno
            passed = True
            break

        # not passing: either stop (report-only) or run the developer + rebuild
        if args.no_fix or itno == args.max_iters:
            report["iterations"].append(it_entry)
            report["iterations_run"] = itno
            break

        # a qa-harness-only failure (claude outage / limits) has nothing for the
        # developer pass to fix in the generator — just re-QA next iteration.
        blocking = [i for i in verdict.get("issues", [])
                    if i["severity"] in ("critical", "major")]
        if blocking and all(i.get("area") == "qa-harness" for i in blocking):
            print("[qa] failure is qa-harness only (claude unavailable); "
                  "skipping developer pass and retrying QA")
            report["iterations"].append(it_entry)
            report["iterations_run"] = itno
            continue

        print(f"[qa] invoking developer pass to fix {verdict.get('blocking_count', 0)} "
              f"blocking issue(s)...")
        dev = dev_pass(claude_exe, repo_root, build_script, verdict, qa_dir,
                       args.dev_minutes, stamp, itno)
        it_entry["dev"] = dev
        print(f"[qa] developer pass: {dev['status']}")

        # rebuild the site with the developer's fixes, ready for the next QA loop
        b_ok, b_tail = run_build(python_exe, build_script, repo_root, args.run_tests,
                                 qa_dir / f"build_{stamp}_iter{itno}.log")
        it_entry["rebuild_ok"] = b_ok
        it_entry["rebuild_tail"] = b_tail
        print(f"[qa] rebuild after fix: {'ok' if b_ok else 'FAILED'}")

        report["iterations"].append(it_entry)
        report["iterations_run"] = itno

    report["passed"] = passed

    # on a clean pass, advance the snapshot so subsequent runs only re-verify
    # genuinely new papers.
    if passed:
        final_status = load_status(webapp)
        if final_status is not None:
            write_json(snapshot_path, final_status)

    json_path, md_path = write_report(qa_dir, stamp, report)
    print(f"\n[qa] {'PASS' if passed else 'FAIL'} after {report['iterations_run']} "
          f"iteration(s)")
    print(f"[qa] report: {json_path}")
    print(f"[qa] report: {md_path}")
    # exit 0 on pass, 1 on fail (so the daily pipeline can gate on it)
    return 0 if passed else 1


if __name__ == "__main__":
    sys.exit(main())
