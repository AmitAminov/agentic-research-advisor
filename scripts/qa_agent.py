#!/usr/bin/env python3
"""
Claude-driven QA loop for the reproduction web app  (pipeline component 5).

The static site is generated by ``build_webapp.py`` into  <repo>/webapp/ .  This
harness closes the loop on its *quality*: it invokes the local ``claude`` CLI
headlessly to inspect the built site and return a structured pass/fail verdict,
and — when the verdict is *fail* — invokes a second, "developer" ``claude`` pass
to fix ``build_webapp.py`` / its inline templates, rebuilds the site, and
re-QAs.  It repeats until the site passes or an iteration budget is exhausted.

What the QA pass checks (read-only inspection of webapp/ + webapp/data/status.json):
  1. NEW paper sections  -- every paper added since the last passing QA run is
     actually present as its own ``papers/<id>.html`` page, correctly linked
     from ``index.html``, and wired into the dashboard search/filter data.
  2. Structure           -- index + per-paper pages exist, assets resolve,
     status.json is internally consistent with the rendered pages.
  3. Flow / design       -- tabs, cards, filters and the dark "scientific" theme
     render coherently; no obviously broken markup or dead links.
  4. Manim animations    -- papers flagged ``has_manim`` expose a working
     Animation tab whose <video>/<img> ``src`` points at a media file that
     exists on disk (the central interactive artefact of each reproduction).
  5. Usability           -- search box, area/status chips, tabs and the figure
     lightbox are present and their JS handlers are wired up.

Design notes / constraints honoured:
  * Windows, CPU-only, Python 3.10 in the project ``.venv``.  Nothing here needs
    a GPU; if a future check needs heavy compute it must scale down and record
    the deviation in the report rather than fail hard.
  * Every ``claude`` invocation is HEADLESS (``claude -p ... --dangerously-skip-
    permissions``) and BOUNDED by a wall-clock timeout; the whole child tree is
    killed on timeout.
  * ``config.json`` is READ-ONLY here (only the orchestrator edits it).
  * ``paper_pipeline.py`` is never touched; its API is preserved.
  * The GitHub token is NEVER read for logic here, and any value that might be a
    token is scrubbed from captured output before it is written to the report.
    Nothing is committed.

Usage:
    python qa_agent.py --config ../config.json
    python qa_agent.py --config ../config.json --max-iters 3 --qa-minutes 15 --dev-minutes 25
    python qa_agent.py --config ../config.json --new-ids DL-some-slug ML-other-slug
    python qa_agent.py --config ../config.json --no-fix          # report only, never invoke the developer pass
    python qa_agent.py --config ../config.json --run-tests       # rebuild with per-paper pytest badges
"""
