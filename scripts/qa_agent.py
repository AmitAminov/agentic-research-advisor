def load_config(path: Path) -> dict[str, Any]:
    """Read config.json. READ-ONLY — this harness never writes it back."""
    return json.loads(path.read_text(encoding="utf-8"))


def read_json(path: Path) -> dict[str, Any] | None:
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except Exception:  # noqa: BLE001
        return None


def write_json(path: Path, obj: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(obj, ensure_ascii=False, indent=2), encoding="utf-8")


# --------------------------------------------------------------------------- #
# token scrubbing — a secret must never reach the report or logs
# --------------------------------------------------------------------------- #
# optional PAT file (github.token_file in config / GITHUB_TOKEN_FILE env);
# main() re-resolves this once the config is loaded.
_TOKEN_FILE: Path | None = pipeline_paths.token_file()
_SECRET_PATTERNS = (
    re.compile(r"gh[pousr]_[A-Za-z0-9]{20,}"),
    re.compile(r"github_pat_[A-Za-z0-9_]{20,}"),
    re.compile(r"x-access-token:[^@\s]+@"),
)


def _secret_values() -> list[str]:
    """Collect concrete secret values to redact (never logged individually)."""
    vals: list[str] = []
    for key in ("GITHUB_TOKEN", "GITHUB_ACCESS_TOKEN", "GH_TOKEN"):
        v = os.environ.get(key)
        if v and len(v) >= 8:
            vals.append(v)
    try:
        # Resolve the real PAT (Secret Manager first, local token file as
        # fallback) purely so it can be redacted from captured output. The value
        # is only ever added to the redaction list; it is never logged.
        t = get_secret(
            "github-pat", env="GITHUB_TOKEN",
            file_fallback=str(_TOKEN_FILE) if _TOKEN_FILE else None,
        ).strip()
        if t and len(t) >= 8:
            vals.append(t)
    except Exception:  # noqa: BLE001
        pass
    return vals


def scrub(text: str) -> str:
    """Redact any concrete token value and token-shaped strings from text."""
    if not text:
        return text
    for v in _secret_values():
        text = text.replace(v, "[REDACTED]")
    for pat in _SECRET_PATTERNS:
        text = pat.sub("[REDACTED]", text)
    return text


def tail_file(path: Path, n: int = 60) -> str:
    try:
        lines = path.read_text(encoding="utf-8", errors="replace").splitlines()
    except Exception:  # noqa: BLE001
        return ""
    return scrub("\n".join(lines[-n:]))


# --------------------------------------------------------------------------- #
# headless claude runner (mirrors reproduce.run_claude)
# --------------------------------------------------------------------------- #
def run_claude(claude_exe: str, repo_root: Path, cwd: Path, prompt: str,
               minutes: int, logf: Path) -> tuple[str, int]:
    """Invoke claude headlessly in ``cwd`` with a hard timeout.

    Returns (status, returncode). The child's env is inherited (claude needs its
    own auth) but is NEVER written anywhere by us. On timeout the whole child
    tree is killed (claude spawns node children on Windows).
    """
    args = [claude_exe, "-p", prompt, "--dangerously-skip-permissions",
            "--add-dir", str(repo_root)]
    logf.parent.mkdir(parents=True, exist_ok=True)
    with logf.open("w", encoding="utf-8", errors="replace") as out:
        try:
            proc = subprocess.Popen(
                args, cwd=str(cwd), stdin=subprocess.DEVNULL,
                stdout=out, stderr=subprocess.STDOUT, text=True,
            )
        except FileNotFoundError:
            return ("claude-not-found", -1)
        try:
            proc.wait(timeout=minutes * 60)
            return ("completed", proc.returncode)
        except subprocess.TimeoutExpired:
            if os.name == "nt":
                # kill the whole tree (claude spawns node children on Windows)
                subprocess.run(["taskkill", "/F", "/T", "/PID", str(proc.pid)],
                               stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
            else:
                proc.kill()
            return ("timeout", -2)


# A claude run that dies on a transient API/limit error leaves a short log with
# one of these signatures and never writes the verdict file.  Without retries
# the loop mislabels the outage as "QA pass produced no parseable verdict".
_TRANSIENT_ERROR_PATTERNS = (
    re.compile(r"API Error:\s*5\d\d", re.IGNORECASE),
    re.compile(r"\boverloaded\b", re.IGNORECASE),
    re.compile(r"session limit", re.IGNORECASE),
    re.compile(r"usage limit", re.IGNORECASE),
    re.compile(r"rate limit", re.IGNORECASE),
)
# a genuine QA/dev run produces far more output than an immediate API failure
_TRANSIENT_LOG_MAX_CHARS = 2000


def _transient_failure(logf: Path) -> str | None:
    """Return the error line if the log shows only a transient API failure."""
    try:
        text = logf.read_text(encoding="utf-8", errors="replace").strip()
    except Exception:  # noqa: BLE001
        return None
    if not text or len(text) > _TRANSIENT_LOG_MAX_CHARS:
        return None
    for pat in _TRANSIENT_ERROR_PATTERNS:
        if pat.search(text):
            return scrub(text.splitlines()[0])
    return None


def run_claude_with_retry(claude_exe: str, repo_root: Path, cwd: Path, prompt: str,
                          minutes: int, logf: Path, attempts: int = 3,
                          wait_s: int = 60) -> tuple[str, int]:
    """run_claude, retrying runs that die instantly on a transient API error.

    Returns run_claude's (status, returncode); if every attempt hits a
    transient error the status is "api-error" so callers can report the real
    outage instead of a generic missing-verdict failure.
    """
    code = -1
    for attempt in range(1, attempts + 1):
        status, code = run_claude(claude_exe, repo_root, cwd, prompt, minutes, logf)
        if status != "completed":
            return (status, code)
        err = _transient_failure(logf)
        if err is None:
            return (status, code)
        if attempt < attempts:
            print(f"[qa] claude run hit a transient error ({err}); "
                  f"retrying in {wait_s}s ({attempt}/{attempts - 1} retries used)")
            time.sleep(wait_s)
    return ("api-error", code)


# --------------------------------------------------------------------------- #
# build runner
# --------------------------------------------------------------------------- #
def run_build(python_exe: str, build_script: Path, repo_root: Path,
              run_tests: bool, logf: Path) -> tuple[bool, str]:
    """Regenerate the static site via build_webapp.py. Returns (ok, tail)."""
    args = [python_exe, str(build_script), "--repo", str(repo_root)]
    if run_tests:
        args += ["--run-tests", "--python", python_exe]
    logf.parent.mkdir(parents=True, exist_ok=True)
    try:
        proc = subprocess.run(args, cwd=str(build_script.parent),
                              capture_output=True, text=True, timeout=1800)
    except FileNotFoundError:
        logf.write_text("build_webapp.py or python interpreter not found",
                        encoding="utf-8")
        return (False, "build interpreter/script not found")
    except subprocess.TimeoutExpired:
        logf.write_text("build timed out (>1800s)", encoding="utf-8")
        return (False, "build timed out (>1800s)")
    out = scrub((proc.stdout or "") + (proc.stderr or ""))
    logf.write_text(out, encoding="utf-8")
    ok = proc.returncode == 0
    tail = "\n".join(out.splitlines()[-40:])
    return (ok, tail)


