def load_status(webapp: Path) -> dict[str, Any] | None:
    return read_json(webapp / "data" / "status.json")


def paper_ids(status: dict[str, Any] | None) -> list[str]:
    if not status:
        return []
    return [p.get("id", "") for p in status.get("papers", []) if p.get("id")]


def compute_new_ids(status: dict[str, Any] | None, snapshot: dict[str, Any] | None,
                    override: list[str] | None) -> list[str]:
    """New paper ids = explicit override, else those absent from the snapshot."""
    if override:
        return sorted(set(override))
    current = set(paper_ids(status))
    if snapshot is None:
        # first ever QA run: treat every paper as "new" so all are verified.
        return sorted(current)
    seen = set(paper_ids(snapshot))
    return sorted(current - seen)


def format_new_sections(new_ids: list[str], status: dict[str, Any] | None) -> str:
    if not new_ids:
        return "  (none detected as new — verify the site as a whole instead.)"
    by_id = {p.get("id"): p for p in (status or {}).get("papers", [])}
    lines: list[str] = []
    for pid in new_ids:
        p = by_id.get(pid, {})
        extra = []
        if p:
            extra.append(f"area={p.get('code', '?')}")
            extra.append(f"has_manim={bool(p.get('has_manim'))}")
            extra.append(f"reproduced_figs={p.get('reproduced_figs', 0)}")
            title = str(p.get("title", ""))[:70]
            head = f"  - {pid}  ({', '.join(extra)})"
            if title:
                head += f"  — {title}"
            lines.append(head)
        else:
            lines.append(f"  - {pid}")
    return "\n".join(lines)


def format_status_summary(status: dict[str, Any] | None) -> str:
    if not status:
        return "  (data/status.json missing or unreadable — this is itself a defect.)"
    return (f"  total={status.get('total')}  reproduced={status.get('reproduced')}  "
            f"animated={status.get('animated')}  tests_passing={status.get('tests_passing')}  "
            f"by_area={status.get('by_area')}  generated={status.get('generated')}")


